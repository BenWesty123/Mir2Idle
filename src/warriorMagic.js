export const WARRIOR_COMBAT_SKILLS = [
  BASIC_ATTACK_SKILL,
  ...CRYSTAL_WARRIOR_SPELLS.filter((spell) => !spell.passive || spell.combatPassive),
];

export function warriorSpellById(spellId) {
  if (spellId === BASIC_ATTACK_SKILL.id) return BASIC_ATTACK_SKILL;
  return CRYSTAL_WARRIOR_SPELLS.find((spell) => spell.id === spellId) ?? null;
}

export function warriorSpellByShape(shape) {
  const id = Number(shape);
  return CRYSTAL_WARRIOR_SPELLS.find((spell) => spell.shape === id) ?? null;
}

export function taoistSpellById(spellId) {
  return CRYSTAL_TAOIST_SPELLS.find((spell) => spell.id === spellId) ?? null;
}

export function taoistSpellByShape(shape) {
  const id = Number(shape);
  return CRYSTAL_TAOIST_SPELLS.find((spell) => spell.shape === id) ?? null;
}

export function magicIconSrc(spell) {
  if (!spell || spell.icon == null) return "";
  const crystalSkillFrame = Number(spell.icon) * 2;
  return `/public/magic-icons/images/frame_${String(crystalSkillFrame).padStart(6, "0")}.png`;
}

export function spellMpCost(spell, learned) {
  if (!spell || spell.id === BASIC_ATTACK_SKILL.id) return 0;
  return Math.max(0, (Number(spell.baseCost) || 0) + (Number(learned?.level) || 0) * (Number(spell.levelCost) || 0));
}

export const CRYSTAL_SPELL_GLOBAL_LOCK_MS = 1800;

export function spellDelayMs(spell, learned) {
  if (!spell || spell.id === BASIC_ATTACK_SKILL.id) return 0;
  if (spell.autoCooldownMs) return Math.max(0, Number(spell.autoCooldownMs) || 0);
  return Math.max(0, (Number(spell.delayBase) || 0) - (Number(learned?.level) || 0) * (Number(spell.delayReduction) || 0));
}

// Crystal uses per-spell UserMagic.GetDelay() and also a global SpellTime lock (1800ms) on cast.
export function crystalSpellCastCooldownMs(spell, learned) {
  return Math.max(CRYSTAL_SPELL_GLOBAL_LOCK_MS, spellDelayMs(spell, learned));
}

export function spellExperienceTarget(spell, level) {
  if (!spell || level >= 3) return 0;
  return [spell.need1, spell.need2, spell.need3][level] ?? 0;
}

export function spellLevelRequirement(spell, level) {
  if (!spell || level >= 3) return 0;
  return [spell.level1, spell.level2, spell.level3][level] ?? 0;
}
