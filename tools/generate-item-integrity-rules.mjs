import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import {
  empowerCandidateRolls,
  itemCanBeEmpowered,
  itemEmpowerLevel,
} from "../src/core/empoweredItems.js";
import { BENEDICTION_MAX_WEAPON_LUCK, CRYSTAL_MAX_LUCK } from "../src/battleData.js";
import { ITEM_INTEGRITY_RULES_VERSION } from "../src/core/itemIntegrityVersion.js";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const itemsPath = join(root, "src/data/items.json");
const outputPath = join(root, "tools/stats-worker/itemRules.generated.js");
const items = JSON.parse(readFileSync(itemsPath, "utf8")).items ?? [];

const SLOT_FLAGS = {
  weapon: 0x0001,
  armour: 0x0002,
  helmet: 0x0004,
  necklace: 0x0008,
  bracelet: 0x0010,
  ring: 0x0020,
  amulet: 0x0040,
  belt: 0x0080,
  boots: 0x0100,
  stone: 0x0200,
};

const DIRECT_SLOTS = new Set([
  "weapon", "armour", "helmet", "torch", "necklace", "amulet",
  "belt", "boots", "stone", "mount", "glyph",
]);

function equipmentSlots(item) {
  const slot = String(item?.slot ?? "");
  if (slot === "bracelet") return ["braceletL", "braceletR"];
  if (slot === "ring") return ["ringL", "ringR"];
  return DIRECT_SLOTS.has(slot) ? [slot] : [];
}

function legacyEmpowerBounds(roll, item) {
  if (roll.min != null && roll.max != null) {
    return {
      min: Number(roll.min),
      max: Number(roll.max),
      step: Number(roll.step ?? 1),
    };
  }
  const level = Math.max(1, Math.min(70, itemEmpowerLevel(item)));
  if (roll.range) {
    return { min: 1, max: level >= 50 ? 4 : level >= 30 ? 3 : 2, step: 1 };
  }
  if (roll.key === "hp" || roll.key === "mp") {
    const min = level >= 50 ? 25 : level >= 30 ? 15 : 10;
    return { min, max: min + (level >= 50 ? 15 : 10), step: 1 };
  }
  return { min: 1, max: level >= 50 ? 3 : 2, step: 1 };
}

function empowerRules(item) {
  if (!itemCanBeEmpowered(item)) return [];
  return empowerCandidateRolls(item).map((roll) => {
    const bounds = legacyEmpowerBounds(roll, item);
    if (roll.spellId && roll.kind) {
      return {
        type: "spell",
        spellId: roll.spellId,
        kind: roll.kind,
        ...bounds,
      };
    }
    return {
      type: "stat",
      key: roll.key,
      range: Boolean(roll.range),
      index: roll.range ? Math.max(0, Math.min(1, Number(roll.index ?? 1))) : null,
      ...bounds,
    };
  });
}

function gemRules(item) {
  const slot = String(item?.slot ?? "");
  const requiredFlag = SLOT_FLAGS[slot] ?? 0;
  const compatible = items.filter((candidate) => {
    const unique = Math.trunc(Number(candidate?.gem?.unique) || 0);
    return requiredFlag && unique && (unique & requiredFlag) !== 0;
  });
  const targets = {};
  let maxUses = 0;
  let allowsDurability = false;
  for (const candidate of compatible) {
    const gem = candidate.gem;
    maxUses = Math.max(maxUses, Math.trunc(Number(gem.criticalDamage) || 0));
    if (Number(gem.durabilityBonus) > 0) {
      allowsDurability = true;
      continue;
    }
    const stat = gem.stat;
    if (!stat?.key) continue;
    const target = stat.range ? `${stat.key}:${Math.max(0, Math.min(1, Number(stat.index ?? 1)))}` : stat.key;
    const amount = Math.max(1, Math.trunc(Number(stat.amount) || 1));
    const maxStatCount = Math.max(0, Math.trunc(Number(gem.maxStatCount) || 0));
    const existing = targets[target] ?? { increments: [], max: 0 };
    if (!existing.increments.includes(amount)) existing.increments.push(amount);
    existing.max = Math.max(existing.max, amount * maxStatCount);
    existing.increments.sort((a, b) => a - b);
    targets[target] = existing;
  }
  return { maxUses, allowsDurability, targets };
}

const rules = {};
for (const item of items) {
  const slots = equipmentSlots(item);
  if (!slots.length) continue;
  rules[item.id] = {
    name: item.name ?? item.id,
    itemSlot: item.slot,
    slots,
    smithCap: 5,
    refineCap: item.slot === "weapon" ? 10 : 0,
    benedictionLuck: item.slot === "weapon"
      ? { min: -CRYSTAL_MAX_LUCK, max: BENEDICTION_MAX_WEAPON_LUCK }
      : null,
    gems: gemRules(item),
    empower: {
      allowed: itemCanBeEmpowered(item),
      maxTier: 4,
      rolls: empowerRules(item),
    },
  };
}

const source = `// Generated by tools/generate-item-integrity-rules.mjs. Do not edit by hand.\n`
  + `export const ITEM_INTEGRITY_RULES_VERSION = ${JSON.stringify(ITEM_INTEGRITY_RULES_VERSION)};\n`
  + `export const ITEM_RULES = ${JSON.stringify(rules)};\n`;

if (process.argv.includes("--check")) {
  const existing = readFileSync(outputPath, "utf8");
  if (existing !== source) {
    console.error("Item-integrity rules are stale. Run: npm run integrity:rules");
    process.exitCode = 1;
  } else {
    console.log(`Item-integrity rules are current (${Object.keys(rules).length} equippable items).`);
  }
} else {
  writeFileSync(outputPath, source, "utf8");
  console.log(`Wrote ${outputPath} (${Object.keys(rules).length} equippable item rules).`);
}
